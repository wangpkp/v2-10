function 怪物Ⅰ_跟随机制() {
    for (let 怪物Ⅰ_个体_引用6 of sprites.allOfKind(SpriteKind.怪物Ⅰ)) {
        if (Math.sqrt((怪物Ⅰ_个体_引用6.x - 主角Ⅰ.x) * (怪物Ⅰ_个体_引用6.x - 主角Ⅰ.x) + (怪物Ⅰ_个体_引用6.y - 主角Ⅰ.y) * (怪物Ⅰ_个体_引用6.y - 主角Ⅰ.y)) <= 90) {
            if (怪物Ⅰ_个体_引用6.x < 主角Ⅰ.x) {
                if (怪物Ⅰ_个体_引用6.vx < 0) {
                    怪物Ⅰ_个体_引用6.ax = 200
                } else if (怪物Ⅰ_个体_引用6.vx >= 0 && Math.abs(怪物Ⅰ_个体_引用6.vx) <= 25) {
                    怪物Ⅰ_个体_引用6.ax = 40
                } else if (怪物Ⅰ_个体_引用6.vx >= 0 && Math.abs(怪物Ⅰ_个体_引用6.vx) >= 25) {
                    怪物Ⅰ_个体_引用6.ax = 0
                }
                
            } else if (怪物Ⅰ_个体_引用6.x > 主角Ⅰ.x) {
                if (怪物Ⅰ_个体_引用6.vx > 0) {
                    怪物Ⅰ_个体_引用6.ax = -200
                } else if (怪物Ⅰ_个体_引用6.vx <= 0 && Math.abs(怪物Ⅰ_个体_引用6.vx) <= 25) {
                    怪物Ⅰ_个体_引用6.ax = -40
                } else if (怪物Ⅰ_个体_引用6.vx <= 0 && Math.abs(怪物Ⅰ_个体_引用6.vx) >= 25) {
                    怪物Ⅰ_个体_引用6.ax = 0
                }
                
            }
            
        } else {
            怪物Ⅰ_个体_引用6.vx = 0
        }
        
    }
}

